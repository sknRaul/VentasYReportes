    public void all(){
        dtm =  (DefaultTableModel) this.jt_data.getModel();
        dtm.setColumnIdentifiers(new Object[]{"Nombre","RFC","Producto","Cantidad","Precio","Total"});
        this.jt_data.setModel(dtm);
        clean();
        cb.Connection("rollback", "root", "1234");
        try {
            rs = cb.query("select * from reportes");
            while(rs.next()){
                dtm.addRow(new Object[]{rs.getString("nombre"),rs.getString("rfc"), rs.getString("producto"), rs.getString("cantidad"), rs.getString("precio"), rs.getString("Total")});
            }
        } catch (Exception e) {
        }
        report("src\\reportes\\Todo.jrxml");
    }
    
    public void products(){
        dtm =  (DefaultTableModel) this.jt_data.getModel();
        dtm.setColumnIdentifiers(new Object[]{"Producto","Cantidad","Total"});
        this.jt_data.setModel(dtm);
        clean();
        cb.Connection("rollback", "root", "1234");
        try {
            rs = cb.query("select producto, sum(cantidad) as cantidad, sum(Total) as total from reportes group by producto;");
            while(rs.next()){
                dtm.addRow(new Object[]{rs.getString("producto"),rs.getString("cantidad"), rs.getString("Total")});
            }
        } catch (Exception e) {
        }
        report("src\\reportes\\TotalProducts.jrxml");
    }
    
